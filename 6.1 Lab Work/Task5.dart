int sum(int a, int b) {
  return a + b;
}

void main() {
  int result = sum(5, 3);
  print('The sum of 5 and 3 is: $result');

  int anotherResult = sum(10, 7);
  print('The sum of 10 and 7 is: $anotherResult');
  int anotherResult1 = sum(110, 7);
  print('The sum of 110 and 7 is: $anotherResult1');
}